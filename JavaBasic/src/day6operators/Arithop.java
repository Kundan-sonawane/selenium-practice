package day6operators;

public class Arithop {

	public static void main(String[] args) {
		// Variables Definition and Initialization
		int number1 = 55, number2 = 13;
		// Addition Operation
		int res = number1 + number2;
		System.out.println("Sum is: " + res);// 68
		// Subtraction Operation
		res = number1 - number2;
		System.out.println("Difference is : " + res);// 42
		// Multiplication Operation
		res = number1 * number2;
		System.out.println("Multiplied value is : " + res);// 715
		// Division Operation
		res = number1 / number2;
		System.out.println("Quotient is : " + res);// 4
		// Modulus Operation
		res = number1 % number2;
		System.out.println("Remainder is : " + res);// 3

	}
}
